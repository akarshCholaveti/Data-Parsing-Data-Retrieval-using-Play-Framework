package com.max2.pojo;

public class Venue {
	
	private String city;
	private String state;
	
	/*
	 * Getters and Setters for Venue Objects
	 */
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
}
