package com.max2.pojo;

public class ColorDetails {
	private String color;
	private int count;
	
	/*
	 * Get Color Value
	 */
	public String getColor() {
		return color;
	}
	/*
	 * Set Color value
	 */
	public void setColor(String color) {
		this.color = color;
	}
	
	/*
	 * Get Count Value
	 */
	public int getCount() {
		return count;
	}
	/*
	 * Set Count Value
	 */
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
